# Consolidação final de stopwords
stops_finais = {p.lower() for p in stop_nltk | stop_spacy | stop_custom | stop_futebol | stop_contexto | stop_alvo}

def preprocessar_texto(texto):
    """Aplica lematização, remoção de pontuação, URLs e stopwords personalizadas."""
    texto = str(texto).lower()
    texto = re.sub(r'http\S+|www\S+|https\S+', '', texto)
    doc = nlp(texto)

    tokens = []
    for t in doc:
        if not t.is_punct and not t.is_digit and not t.is_space:
            lema = t.lemma_.lower().strip()

            if lema == '' or lema == '-pron-':
                lema = t.text.lower().strip()

            txt_orig = t.text.lower().strip()

            if lema not in stops_finais and txt_orig not in stops_finais:
                if len(lema) > 2 and lema.isalpha():
                    tokens.append(lema)

    return tokens


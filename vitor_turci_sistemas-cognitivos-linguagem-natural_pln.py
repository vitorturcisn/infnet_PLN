def buscar_noticias(termo_busca, top_n=3):
    tokens_busca = preprocessar_texto(termo_busca)
    query_limpa = ' '.join(tokens_busca)

    vetor_busca = vetorizador.transform([query_limpa])

    similaridades = cosine_similarity(vetor_busca, matriz_tfidf).flatten()
    indices_top = similaridades.argsort()[::-1][:top_n]

    print(f"Consulta: '{termo_busca}'")
    print("-" * 60)
    for i, idx in enumerate(indices_top):
        score = similaridades[idx]
        if score > 0:
            print(f"{i+1}º Lugar (Similaridade: {score:.1%})")
            print(f"Título: {df_fla['title'].iloc[idx]}")
            print(f"Trecho: {df_fla['text'].iloc[idx][:150]}...\n")
        else:
            print("Nenhum documento relevante encontrado.")
            break

